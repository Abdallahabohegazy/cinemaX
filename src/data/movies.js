export const generateSeatMap = (bookedSeats = []) => {
  const { rows, seatsPerRow, seatTypes } = SEAT_MAP_CONFIG
  return rows.map(row => ({
    row,
    type: seatTypes[row],
    seats: Array.from({ length: seatsPerRow }, (_, i) => {
      const seatId = `${row}${i + 1}`
      return {
        id: seatId,
        row,
        number: i + 1,
        type: seatTypes[row],
        status: bookedSeats.includes(seatId) ? 'booked' : 'available',
      }
    }),
  }))
}
